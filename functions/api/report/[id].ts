async function generatePDF(env: Env, report: Record<string, unknown>): Promise<ArrayBuffer> {
  const vector8d = JSON.parse(report.vector_8d as string);
  const matches = JSON.parse(report.historical_matches as string);

  // Use Workers AI to generate personalized content
  const insights = await generateInsights(env, vector8d, matches);

  // For now, return a placeholder
  // In production, use jsPDF or a PDF generation service
  const pdfContent = `
THE REALM OF PATTERNS
====================

Your 16D Identity Report
------------------------

Vector: ${vector8d.map((v: number) => v.toFixed(2)).join(', ')}

${insights}

Historical Resonance:
${matches.slice(0, 5).map((m: { name: string; resonance: number }) =>
    `- ${m.name} (${(m.resonance * 100).toFixed(0)}% resonance)`
  ).join('\n')}

---
Generated by The Realm of Patterns
https://therealmofpatterns.pages.dev
`;

  // Convert to ArrayBuffer (in production, use actual PDF generation)
  const encoder = new TextEncoder();
  const bytes = encoder.encode(pdfContent);
  const out = new Uint8Array(bytes.byteLength);
  out.set(bytes);
  return out.buffer;
}

async function generateInsights(
  env: Env,
  vector: number[],
  _matches: { name: string; resonance: number; quote: string }[]
): Promise<string> {
  try {
    const prompt = `You are a cosmic identity guide. Based on this 8D identity vector: [${vector.map(v => v.toFixed(2)).join(', ')}],
    where dimensions are P (Phase/Identity), E (Existence/Structure), μ (Cognition), V (Value/Beauty),
    N (Expansion), Δ (Action), R (Relation), Φ (Field/Witness).

    The highest value indicates the dominant dimension. Write a 2-paragraph personalized insight about this person's
    cosmic signature and their potential for growth. Be specific and meaningful. Do not use generic language.`;

    const result = await env.AI.run('@cf/meta/llama-3.1-8b-instruct' as any, {
      prompt,
      max_tokens: 500,
    });

    return (result as { response: string }).response || 'Your unique cosmic pattern reveals profound potential...';
  } catch (error) {
    console.error('AI insight generation failed:', error);
    return 'Your unique cosmic pattern reveals profound potential for growth and self-discovery.';
  }
}

// ============================================
// Error Responses
// ============================================

function unauthorizedResponse(message: string): Response {
  const error: ErrorResponse = {
    success: false,
    error: { code: 'UNAUTHORIZED', message },
  };
  return new Response(JSON.stringify(error), {
    status: 401,
    headers: { 'Content-Type': 'application/json' },
  });
}

function forbiddenResponse(message: string): Response {
  const error: ErrorResponse = {
    success: false,
    error: { code: 'FORBIDDEN', message },
  };
  return new Response(JSON.stringify(error), {
    status: 403,
    headers: { 'Content-Type': 'application/json' },
  });
}

function notFoundResponse(message: string): Response {
  const error: ErrorResponse = {
    success: false,
    error: { code: 'NOT_FOUND', message },
  };
  return new Response(JSON.stringify(error), {
    status: 404,
    headers: { 'Content-Type': 'application/json' },
  });
}
