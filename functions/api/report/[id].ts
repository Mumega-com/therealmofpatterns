/**
 * GET /api/report/:id
 * Download a generated PDF report
 */

import { Env, ErrorResponse } from '../../../src/types';

export const onRequestGet: PagesFunction<Env> = async (context) => {
  const { params, request, env } = context;
  const reportId = params.id as string;

  try {
    // Check authorization
    const authHeader = request.headers.get('Authorization');
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      return unauthorizedResponse('Missing authorization token');
    }

    const token = authHeader.split(' ')[1];

    // Verify session token
    const sessionData = await env.CACHE.get(`session:${token}`);
    if (!sessionData) {
      return unauthorizedResponse('Invalid or expired session');
    }

    const session = JSON.parse(sessionData);

    // Verify report belongs to this session
    if (session.reportId !== reportId) {
      return forbiddenResponse('Access denied to this report');
    }

    // Fetch report from database
    const report = await env.DB.prepare(`
      SELECT * FROM reports WHERE id = ?
    `).bind(reportId).first();

    if (!report) {
      return notFoundResponse('Report not found');
    }

    // Check if PDF exists in R2
    const pdfKey = `reports/${reportId}.pdf`;
    const pdfObject = await env.STORAGE.get(pdfKey);

    if (pdfObject) {
      // Return existing PDF
      return new Response(pdfObject.body, {
        headers: {
          'Content-Type': 'application/pdf',
          'Content-Disposition': `attachment; filename="realm-of-patterns-${reportId.slice(0, 8)}.pdf"`,
          'Cache-Control': 'private, max-age=3600',
        },
      });
    }

    // PDF doesn't exist yet - generate it
    // (In production, this would be handled by the webhook, but fallback here)
    const pdfBuffer = await generatePDF(env, report);

    // Store in R2
    await env.STORAGE.put(pdfKey, pdfBuffer, {
      httpMetadata: { contentType: 'application/pdf' },
    });

    return new Response(pdfBuffer, {
      headers: {
        'Content-Type': 'application/pdf',
        'Content-Disposition': `attachment; filename="realm-of-patterns-${reportId.slice(0, 8)}.pdf"`,
      },
    });
  } catch (error) {
    console.error('Report download error:', error);
    const errorResponse: ErrorResponse = {
      success: false,
      error: {
        code: 'INTERNAL_ERROR',
        message: 'Failed to retrieve report.',
      },
    };
    return new Response(JSON.stringify(errorResponse), {
      status: 500,
      headers: { 'Content-Type': 'application/json' },
    });
  }
};

